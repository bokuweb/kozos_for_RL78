/******************************************************************//**
 * @brief       putc
 * @param       none
 * @return      none
 *********************************************************************/
int16_t kz_putc(uint8_t c)
{
    if (c == '\n')
        serial_send_byte('\r');
    return serial_send_byte(c);
}

/******************************************************************//**
 * @brief       puts
 * @param       none
 * @return      none
 *********************************************************************/
int16_t kz_puts(uint8_t *str)
{
    while (*str)
        kz_putc (*(str++));
    return 0;
}
